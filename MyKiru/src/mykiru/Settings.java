    public void FindDir(){
        try {
        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(javax.swing.JFileChooser.DIRECTORIES_ONLY);
        Path cdd = Paths.get(UO_DIR.getText());
        File cdd1 = cdd.toFile();
        //chooser.setName("Choose your Ultima Online Directory");
        chooser.setDialogTitle("Choose your Ultima Online Directory");
        chooser.setCurrentDirectory(cdd1);
        chooser.showOpenDialog(null);
        File cd = chooser.getSelectedFile();
        String fp = cd.getAbsolutePath();
        UO_DIR.setText(fp);
        } catch (Exception e) {}
    }
    
