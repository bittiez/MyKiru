    public void savereg(){
        if(Razor.isSelected()) {fs.w_reg("Razor", "true");} else {fs.w_reg("Razor", "false");}
        if(AssistUO.isSelected()) {fs.w_reg("AssistUO", "true");} else {fs.w_reg("AssistUO", "false");}
        if(Client.isSelected()) {fs.w_reg("Client", "true");} else {fs.w_reg("Client", "false");}
        if(None.isSelected()) {fs.w_reg("None", "true");} else {fs.w_reg("None", "false");}
        if(Close.isSelected()) {fs.w_reg("Close", "true");} else {fs.w_reg("Close", "false");}
        fs.w_reg("UO_DIR", UO_DIR.getText());
        fs.w_reg("Allowed_Types", allowed.getText());
    }
    public void loadreg(){
        if("true".equals(fs.r_reg("Razor", "false"))) {Razor.setSelected(true);}
        if("true".equals(fs.r_reg("AssistUO", "false"))) {AssistUO.setSelected(true);}
        if("true".equals(fs.r_reg("Client", "false"))) {Client.setSelected(true);}
        if("true".equals(fs.r_reg("None", "false"))) {None.setSelected(true);}
        if("true".equals(fs.r_reg("Close", "false"))) {Close.setSelected(true);}
        UO_DIR.setText(fs.r_reg("UO_DIR", ""));
        allowed.setText(fs.r_reg("Allowed_Types", "exe, mul, cfg, idx, def, zip"));
    }
    
